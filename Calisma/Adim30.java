
public class Adim30 {

	public static void main(String[] args) {
		System.out.println("Birinci sayý: 7");
		System.out.println();
		katlari(7);
		System.out.println();

		System.out.println("Ýkinci sayý: 8");
		System.out.println();
		katlari(8);
		System.out.println();
	}

	public static void katlari(int sayi) {

		System.out.println("Ýki katý: " + (2 * sayi));
		System.out.println("Üç katý: " + (3 * sayi));
		System.out.println("Dört katý:" + (4 * sayi));
		System.out.println("Beþ katý:" + (5 * sayi));

	}
}
